void Widget::fill( int x,  int y){
   // pathingMap_.fillCell(x,y);

    // draw rectangle
    QGraphicsRectItem* rect = new QGraphicsRectItem(0,0, 25, 25);
    rect->setPos(x*25,y*25);
    QBrush brush;
    brush.setColor(Qt::blue);
    brush.setStyle(Qt::SolidPattern);
    rect->setBrush(brush);
    scene->addItem(rect);
}

void Widget::fillBall(int x, int y){

    QGraphicsEllipseItem* ball = new QGraphicsEllipseItem(0, 0, 7, 7);
    ball->setPos(9+x*25, 9+y*25);
    QBrush brush;
    brush.setColor(Qt::red);
    brush.setStyle(Qt::SolidPattern);
    ball->setBrush(brush);
    scene->addItem(ball);
}



void Widget::drawMap(const std::vector<std::vector<int> > &vec){
    for (int y = 0; y < 21; y++){
        for (int x = 0; x < 19; x++){
            if (vec[y][x] != 0){
                fill(x,y);
            }
            else
                fillBall(x,y);
        }
    }
}





